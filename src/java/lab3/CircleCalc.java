public class CircleCalc {
    private double radius;
    
    public CircleCalc(){}

    public CircleCalc(double radius) {
        this.radius = radius;
    }

    public double getRadius() {
        return radius;
    }

    public void setRadius(double radius) {
        this.radius = radius;
    }
    
    public double getArea(){
        return (radius * radius) * 3.14;
    }
}
